#include "libft.h"

static int	is_alpha(int c)
{
	if (('a' <= c && c <= 'z') || ('A' <= c && c <= 'Z'))
		return (1);
	return (0);
}

int	ft_isalnum(int c)
{
	if (('0' <= c && c <= '9') || (is_alpha(c)))
	{
		return (1);
	}
	return (0);
}
